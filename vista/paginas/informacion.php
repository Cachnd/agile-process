
<h2><span><center>Bienvenid@s</center></span></h2><p>

 <p>La Competencia Internacional Universitaria de Programación ACM (en inglés ACM International Collegiate Programming Contest, abreviado ICPC) brinda la oportunidad para que universitarios de distintas universidades puedan interactuar entre ellos, y así perfeccionar y demostrar sus habilidades: en resolución de problemas, programación y trabajo en equipo. Además el torneo da la oportunidad para alentar y dar a conocer la próxima generación de profesionales de la informática que persiguen la excelencia.<br />
<br />
En este torneo participan casi 2.000 universidades desde más de 80 países en los seis continentes, por lo que se hace necesario separar el proceso en distintas fases clasificatorias regionales. En el mundo hay más de 30 regiones distribuidas geográficamente.<br />
<br />
La final mundial se celebra cada año en un lugar distinto, normalmente hoteles de lujo, y congrega a los equipos ganadores de todas las competiciones regionales.<br />
