	<div id="titulopn">Apelaciones</div>
	<div id="textnot">
	  <div align="justify">Los competidores pueden reportar violaciones del reglamento o falta de conducta en la competencia dentro de 7 días despu&eacute;s de la realizaci&oacute;n de la competencia al Director de Sede, el cual realizará una recomendaci&oacute;n al Comit&eacute; Principal de la ACM-ICPC - Bolivia. Este comit&eacute;, puede por 2/3 de los votos de sus miembros tomar una decisi&oacute;n y enviarla al Director. Solo puede observarse la violación del reglamento o falta de conductas graves. La decisi&oacute;n de los jurados de aceptar o rechazar el reporte es final.</div>
	</div>
	<div id="titulopn">Actividades</div>
	<div id="textnot">
	  <p align="justify">Todos los competidores pueden participar en todas las actividades requeridas: </p>
