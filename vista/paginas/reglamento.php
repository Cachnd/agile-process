	  <div id="textnot">
	  <p align="justify">La Competencia Internacional de Programaci&oacute;n (ACM ICPC) provee a los estudiantes  universitarios una oportunidad de demostrar sus habilidades para solucionar problemas en la computadora, recibiendo reconocimientos de acuerdo a estas. </p>
	  <p align="justify">Sobre todo, este provee a todos los equipos competidores con iguales condiciones la oportunidad de participar en la final sudamericana de programaci&oacute;n.</p>
	</div>
	<div id="titulopn" align="justify">
	  Descripci&oacute;n General</div>
	<div id="textnot">
	  <p align="justify">Durante la competencia, los equipos compuestos por 3 estudiantes trabajan en 8 a 10 problemas, buscando implementar algoritmos para solucionarlos en un tiempo m&aacute;ximo de 5 horas.</p>
	  <p align="justify">Los mejores equipos que participen en la <strong>Primera Fase Bolivia</strong>  clasifican autom&aacute;ticamente a la final sudamericana de Programaci&oacute;n. </p>
	</div>
	<div id="titulopn">Organizaci&oacute;n</div>
	<div id="textnot">
	  <p align="justify">Las reglas de la Competencia son revisadas por el Comit&eacute; de la ACM ICPC, presidido por el Director Internacional de la Competencia Ph.D. William B. Poucher de la Universidad Baylor (Waco-Texas). El Director de la Competencia nombra Directores Regionales, quienes conforman el Comit&eacute; Regional de la ACM-ICPC, quienes son responsables  de adoptar las reglas a sus respectivas regiones.</p>
	  <p align="justify">El Comit&eacute; Regional Sudamericano esta conformado por:<br />
		Carlos Ferreira, Director de Brazil (Universidad de Campinhas  Sao Paulo)<br />
		Irene Loisseau, Directora de Argentina (Universidad de Buenos Aires  Buenos Aires)<br />
		Ricardo Baeza-Yates, Director de Chile<br />
		Willmar A. Pimentel, Director de Bolivia<br />
		Ricardo Dahab, Director Regional </p>
	  </div>
	 <div id="titulopn">Condiciones de participaci&oacute;n</div>
	<div id="textnot">
	  <p align="justify">Estudiantes universitarios de pregrado y graduados en el área de las Ciencias de la Computaci&oacute;n y &aacute;reas relacionadas de instituciones de educaci&oacute;n superior  (Universidades, Institutos, Tecnol&oacute;gicos, etc.) pueden participar en la competencia.</p>
	  <p align="justify">Los estudiantes que deseen participar deber&aacute;n conformar equipos de tres miembros de manera obligatoria. Se permitir&aacute; un m&aacute;ximo de un estudiante graduado por equipo, adem&aacute;s no debe tener m&aacute;s de dos a&ntilde;os de haberse graduado de su universidad. </p>
	  <p align="justify">Para aceptar la inscripci&oacute;n de cada miembro debe presentarse un formulario con los datos de cada participante siendo este enviado por el docente coordinador al Comit&eacute; Organizador. Este docente act&uacute;a como Entrenador o Coach, esta persona ser&aacute; el representante oficial del Equipo y ser&aacute; el medio de contacto entre el equipo y los organizadores. Esta persona garantiza la elegibilidad de los miembros del equipo. </p>
	  <p align="justify">Este a&ntilde;o cada universidad podr&aacute; inscribir un m&aacute;ximo de 3 equipos de una misma carrera y 5 si pertenecen a diferentes carreras o facultades. Este es el l&iacute;mite de inscritos que puede aceptarse. Si el n&uacute;mero de equipos postulantes es mayor al de la capacidad de la sede se considerar&aacute;: </p>
	  <div align="justify">
		<ol>
			<li><strong>Cobertura: </strong> A lo mucho, el comit&eacute; organizador seleccionara equipos de diferentes universidades. </li>
			<li><strong>Fecha de Inscripci&oacute;n: </strong> Los primeros equipos en inscribirse tendr&aacute;n prioridad. </li>
		</ol>
	  </div>
	  <p align="justify">Si no se cumple alguna de las siguientes reglas ser&aacute;n considerados inelegibles. </p>
	</div>
	<div id="titulopn">Procedimientos de la Competencia de Programaci&oacute;n</div>
	<div id="textnot">  
	  <ol>
		<li>
		  <div align="justify"><strong>Sitios.-</strong> La Competencia Regional Sudamericana de Programación - Primera fase Bolivia para las universidades miembros del CEUB e invitadas se desarrolla en la universidad sede de la <strong>CCBOL</strong>. <br />
			<br />
		  Para las unviersidades privadas se habilitar&aacute;n sedes en cada departamento (si existiesen postulantes) que realizar&aacute;n la competencia con la sede de la <strong>CCBOL</strong>.<br />
			<br />
		  Todas las sedes que se habiliten <em>(el 2008 existen 3)</em> tendr&aacute;n el mismo nivel y los equipos participantes podr&aacute;n participar en cualquiera de ellas indistintamente, notficando de ello al comit&eacute; central previamente para evitar que sobrepasen el l&iacute;mite m&aacute;ximo de participantes por universidad no importando en cuantas sedes participen.</div>
		</li>
		<li> 
		  <div align="justify"><strong>Idioma</strong>.-        El idioma oficial del Torneo es el Ingles, por lo que el material distribuido y la comunicación durante la competencia seran en este idioma.</div>
		</li>
		<li> 
		  <div align="justify"><strong>Material.-</strong> El participante puede usar recursos impresos, como libros, manuales y listas de programas. Información almacenada magn&eacute;ticamente o cualquier tipo de software es prohibido.</div>
		</li>
		<li> 
		  <div align="justify"><strong>Entorno de computaci&oacute;n.-</strong> Los lenguajes de programación de la competencia son: <a href="http://www.gcc.org" target="_blank">C, C++</a> y <a href="http://java.sun.com" target="_blank">Java</a>.
				Se usa preferentemente el Sistema Operativo Linux, con el IDE <a href="http://www.eclipse.org" target="_blank">Eclipse + CDT</a> que posee la capacidad de soportar los lenguajes de programaci&oacute;n permitidos, con sus compiladores JDK oficial de la SUN y las versiones GNU de los compiladores C/C++.
				Cada equipo puede usar una computadora. </div>
		</li>
		<li>
		  <div align="justify"><strong>Soporte de Sistema.-</strong> Debe existir un equipo de soporte t&eacute;cnico mientras dure la competencia,
				  para solucionar los problemas que ocurriesen en su desarrollo.</div>
		</li>
		<li>
		  <div align="justify"><strong> La competencia.-</strong> La competencia de programación consiste en un conjunto de 8 a 10
				problemas que serán resueltos en 5 horas.
				
	Los equipos pueden enviar una posible solución del problema al jurado de la competencia, quienes pueden aceptar o rechazar la soluci&oacute;n. En caso de rechazar, nuevas soluciones pueden ser enviadas (ver calificaci&oacute;n abajo).
				
	Si un competidor encuentra un error en un problema o alguna ambigüedad, puede realizar una consulta llenando un <em>Clarification Request</em> a los jurados, si ellos est&aacute;n de acuerdo con la observaci&oacute;n o existiese un error, la aclaraci&oacute;n se hará a todos los participantes.</div>
		</li>
		<li> 
		  <div align="justify"><strong>Disciplina.-</strong> Los competidores no pueden hablar con nadie mas que con sus 
				compañeros o personal asignado por el Director de la Competencia.
				
	Los problemas t&eacute;cnicos que podr&iacute;an ocurrir deben ser comunicados al personal de soporte t&eacute;cnico de acuerdo a procedimientos pre-establecidos.
				
	Cualquier conducta irregular que perjudique el desarrollo de la competencia, de acuerdo al criterio del Director de la Competencia, puede causar la descalificaci&oacute;n del equipo.</div>
		</li>
		<li> 
		  <div align="justify"><strong>Alteraciones de la duraci&oacute;n de la Competencia.-</strong> El Director de la Competencia puede alterar la duraci&oacute;n de la 
				competencia si hubiesen ocurrido eventos no planificados que perjudicaron su normal desarrollo, si esto ocurriese, todos los competidores deben ser comunicados de manera uniforme.</div>
		</li>
		<li>
		  <div align="justify"><strong>Calificación</strong>.-          El primer criterio de selección para clasificar a los equipos es el n&uacute;mero de
				problemas resueltos correctamente. Una soluci&oacute;n puede ser considerada incorrecta si: </div>
		</li>
	  </ol>
	  <div align="justify">
		<ul>
			<ul>
				<li> El programa no compila correctamente.</li>
					<li>El tiempo de ejecuci&oacute;n excede el tiempo m&aacute;ximo establecido u ocurre un error en tiempo de ejecuci&oacute;n.</li>
					<li> El programa provee una respuesta incorrecta.</li>
					<li> El formato de salida es diferente del especificado en la hoja de problemas. </li>
			</ul>
		</ul>
	  </div>
	  <p align="justify">Si no se cumple la primera condici&oacute;n, ósea que más de un equipo resolvió el mismo número de problemas, entonces se clasifica por el menor tiempo que uso un equipo para resolver los problemas.</p>
	  <p align="justify">El tiempo total  es la suma de los tiempos usados para resolver cada problema correctamente. El tiempo consumido para resolver el problema es el tiempo transcurrido desde el inicio de la competencia hasta el momento de la aceptaci&oacute;n de un envi&oacute; de soluci&oacute;n, adem&aacute;s de 20 minutos de penalidad por cada envi&oacute; rechazado <em>(respuesta err&oacute;nea)</em>. No se considera el consumo de tiempo de un problema no resuelto.</p>
	  <p align="justify">El Comité de Jurados de la Competencia de Programaci&oacute;n es el &uacute;nico responsable en decidir si las soluciones son correctas. Una vez consultado con ellos, el Director determina a los ganadores. El Director de la Competencia y el Comit&eacute; del Jurado, tienen el poder de tomar decisiones sobre casos no previstos y su decisi&oacute;n es inapelable. </p>
	  <div id="titulopn">Clasificaci&oacute;n</div>
	  <div id="div2">
		<div align="justify">
		  <p>Existen 54 cupos para la Final Sudamericana:</p>
		  <ul>
			<li>Cada universidad que haya confirmado su participaci&oacute;n con anterioridad y que inscriba dos equipos como m&iacute;nimo, tiene derecho a un cupo directo a la final sudamericana. Dicho cupo ser&aacute; asignado al equipo que mejor desempe&ntilde;o tuvo en la Competencia durante la primera fase. Creando un conjunto de cupos preasignados a las universidades participantes.</li>
			<li>Los restantes cupos ser&aacute;n asignados de acuerdo a la clasificaci&oacute;n general de la competencia.</li>
			<li>Si una universidad obtiene m&aacute;s de 3 equipos dentro del grupo de los clasificados, solo se tomar&aacute; en cuenta a los 3 mejores, asign&aacute;ndosele el cupo al siguiente mejor clasificado.  <br />
			</li>
		  </ul>
		  </div>
