		<ul>
			<li>Sesi&oacute;n de presentaci&oacute;n. </li>
			<li>Sesi&oacute;n de Practica (Warm Up) </li>
			<li>Competencia. </li>
			</ul>
	  </div>
	  <p align="justify">La ausencia injustificada descalifica autom&aacute;ticamente al equipo.</p>
